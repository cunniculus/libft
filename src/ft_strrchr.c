#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	len;

	if (c > 255)
		return ((char *)s);
	len = ft_strlen(s);
	while (len >= 0)
	{
		if (s[len] == c)
			return ((char *)(s + len));
		len--;
	}
	return (NULL);
}
